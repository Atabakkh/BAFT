package ir.ata.baft.model;

public class Node {

	private String vertex;
	private double weight;
	
	public Node(){
	}
	
	public String getVertex()
	{
		return vertex;
	}
	
	public void setVertex(String value)
	{
		vertex = value;
	}
		
	public double  getWeight()
	{
		return weight;
	} 
	
	public void setWeight(double value)
	{
		weight = value;
	}

}
